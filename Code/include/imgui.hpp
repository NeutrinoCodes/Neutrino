#ifndef imgui_hpp
#define imgui_hpp

#include "neutrino.hpp"

//////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////// IMGUI header files ////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////
#include <imgui.h>
#include <imgui_impl_glfw.h>
#include <imgui_impl_opengl3.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////// IMPLOT header files ////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////
#include <implot.h>
#include <implot_internal.h>

namespace nu
{
///////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////// "opengl" class ///////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////
class imgui : public neutrino                                                                       /// @brief **ImGui HUD.**
{
private:

public:
  /// @brief **Class constructor.**
  /// @details It does nothing.
  imgui ();

  /////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////// PUBLIC METHODS //////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////
  /// @brief **Begin method.**
  /// @details To be invoked by the user in order to start using Imgui inside the application loop.
  void begin ();

  void window (
               std::string loc_name,
               int         loc_width
              );

  void parameter (
                  std::string loc_description,
                  std::string loc_unit,
                  std::string loc_name,
                  float       loc_value
                 );

  void slider ();

  void plot ();

  void button ();

  void finish ();

  /// @brief **End method.**
  /// @details To be invoked by the user in order to stop using Imgui inside the application loop.
  void end ();

  /// @brief **Class destructor.**
  /// @details Terminates the GLFW context.
  ~imgui();
};
}

#endif