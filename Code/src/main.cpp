/// @file

#include "openclgl.hpp"
#include "utilities.hpp"
#include "program.hpp"

/// These files are relative to the NEUTRINO_PATH environmental variable:
#define POINT_VERTEX_FILE       "/Code/shader/vertex.vert"
#define POINT_FRAGMENT_FILE     "/Code/shader/fragment.frag"
#define TEXT_VERTEX_FILE        "/Code/shader/text_vertex.vert"
#define TEXT_FRAGMENT_FILE      "/Code/shader/text_fragment.frag"

int main()
{
  double  tic;                                                                  // Initial loop time [us]...
  double  toc;                                                                  // Final loop time [us]...

  get_neutrino_path();                                                          // Getting Neutrino installation path...
  load_point_vertex(NEUTRINO_PATH, POINT_VERTEX_FILE);                          // Loading point vertex shader source...
  load_point_fragment(NEUTRINO_PATH, POINT_FRAGMENT_FILE);                      // Loading point fragment shader source...
  load_text_vertex(NEUTRINO_PATH, TEXT_VERTEX_FILE);                            // Loading text vertex shader source...
  load_text_fragment(NEUTRINO_PATH, TEXT_FRAGMENT_FILE);                        // Loading text fragment shader source...

  init_opengl_context();
  init_opencl_context();                                                        // Initializing OpenCL context...

  setup();                                                                      // Executing Neutrino "setup" function...

  while (!glfwWindowShouldClose(window))                                        // Opening window...
  {
    tic = getCPUTime();                                                         // Getting "tic" [us]...
    arcball();                                                                  // Evaluating arcball...
    clear_screen();                                                             // Clearing screen...
    loop();                                                                     // Executing Neutrino "loop" function...
    refresh_screen();                                                           // Refreshing screen...
    toc = getCPUTime();                                                         // Getting "toc" [us]...
    ascii_spin("Action: running OpenCL program...", tic, toc);                  // Displaying loop duration [us]...
  }

  ascii_spin_stop();                                                            // Stopping loop duration display...
  release_opengl_context();                                                     // Releasing OpenGL context...
	release_opencl_context();                                                     // Releasing OpenCL context...
  terminate();                                                                  // Terminating Neutrino application...

  return 0;
}
