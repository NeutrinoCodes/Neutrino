//////////////////////////////////////////////////////////////////////////////////
////////////////////////////////// "POINT" CLASS /////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////
point::point()
{
// Doing nothing.
}

void point::init (
                  GLsizeiptr loc_size                                           // Data size.
                 )
{
  point_structure* data = new point_structure[loc_size];
  size = loc_size;
}

point::~point()
{
  delete[] data;
}

